<template lang="html">
  <div class="student-jobprofile-page">
    <div v-if="showData">

      <div class="columns is-multiline">

        <div class="column is-one-third" v-for="jobs in jobProfiles" v-if="!jobsNull">
          <div class="card">

            <div class="card-header">
              <p class="card-header-title">
                {{ jobs.job_title }}
              </p>

              <span>
                <span v-if="jobs.status == 'application'" class="tag is-success">{{jobs.status}}</span>
                <span v-if="jobs.status == 'closed'" class="tag is-light">{{jobs.status}}</span>
              </span>

            </div>
            <div class="card-content">
              <div class="content">
                Company:&nbsp;<b>{{jobs.company.company_name}}</b><br>
                Location:&nbsp;<b>{{jobs.location}}</b>
              </div>
            </div>
            <footer class="card-footer">
              <router-link :to="{ name: 'view-job-profile', params: { placement_id: jobs.placement_id } }" class="card-footer-item">View</router-link>
            </footer>
          </div>

      </div>
      <div class="column is-full" v-if="jobsNull">
        <div class="media media-card">
          There are no jobs to follow.
        </div>
      </div>

    </div>
  </div>

  <div v-if="!showData">
    <div class="box">
      <h3 class="title">No Data to show.</h3>
    </div>
  </div>
  
</div>
</template>

<script>
import user from '@/api/user';
import Auth from '@/packages/auth/Auth';

export default {
  name: 'student-jobprofile-page',
  created() {
    this.getJobProfile();
  },

  data() {
    return {
      showData: true,
      jobProfiles: [],
      jobsNull: false
    }
  },

  methods: {

    getJobProfile() {
      user.getUserJobProfile(this.getUserId())
      .then((response) => {
        if(response.data[0] == null) {
          this.jobsNull = true;
        }
        else {
          this.jobProfiles = response.data;
        }
      })
      .catch((error) => {
        if(error.response.data.message == 'No placement Primary Found!') {
          this.showData = false;
        }
        else {
          console.log(error);
        }
      })
    },

    getUserId() {
      return Auth.getUserToken();
    }

  }
}
</script>

